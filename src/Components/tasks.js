// 1. You have created a list of pens: 'Blue Pen', 'Red Pen', 'Green Pen', 'Black Pen'. 
// Now use a loop to print the names of all the pens. Write a program to do that.

const pens = ['Blue Pen', 'Red Pen', 'Green Pen', 'Black Pen'];
for (const pen of pens) {
    console.log(pen);
}

